package com.shop.util;

public class NumProduct {
	String name;
	int num;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public NumProduct(String name, int num) {
		super();
		this.name = name;
		this.num = num;
	}
	public NumProduct() {
		super();
	}
	
	
}
